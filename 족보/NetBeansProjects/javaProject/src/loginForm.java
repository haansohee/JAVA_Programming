    private void btnCheckIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckIDActionPerformed
        UserDAO userDAO = new UserDAO();
        
        String userID = txtUserID.getText();
        if(userID == null || userID.equals("")){
            JOptionPane.showMessageDialog(null, "아이디를 입력해 주십시오.", "경고", JOptionPane.ERROR_MESSAGE);
        } else {
            int result = userDAO.idCheck(userID);
            if(result == 1) {
                System.out.println("사용할 수 있는 ID");
                JOptionPane.showMessageDialog(null, "사용할 수 있는 ID");
                boolJoin = true;
            } else {
                System.out.println("아이디 중복");
                JOptionPane.showMessageDialog(null, "아이디 중복", "경고", JOptionPane.ERROR_MESSAGE);

            }
        }        
    }//GEN-LAST:event_btnCheckIDActionPerformed

    private void btnJoinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJoinActionPerformed
        if(boolJoin == false) {
            System.out.println("ID 중복 확인 먼저 해 주십시오.");
            JOptionPane.showMessageDialog(null, "ID 중복 확인 먼저 해 주십시오.", "경고", JOptionPane.ERROR_MESSAGE);
        } else {
            String userName = txtUserName.getText();
            String userID = txtUserID.getText();
            String userPWD = null;
            userPWD += txtUserPWD.getPassword();
            String userEmail = txtUserEmail.getText();
            
            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
            Date date = new Date();
            String userCreated = dateFormat.format(date);
            
            User user = new User(userName, userID, userPWD, userEmail, userCreated);
            UserDAO userDAO = new UserDAO();
            
            int result = userDAO.join(user);
            if(result != 1) {
                System.out.println("회원가입 실패");
                JOptionPane.showMessageDialog(null, "회원가입 실패", "경고", JOptionPane.ERROR_MESSAGE);
            } else {
                System.out.println("회원가입 성공");
                JOptionPane.showMessageDialog(null, "회원가입 성공");
            }
        }
    }//GEN-LAST:event_btnJoinActionPerformed

