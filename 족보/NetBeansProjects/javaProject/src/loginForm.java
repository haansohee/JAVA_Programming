    private void btnRootLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRootLoginActionPerformed
        String rootId = txtRootID.getText();
        String rootPWD = null;
        rootPWD += txtRootPWD.getPassword();
        
        User user = new User();
        UserDAO userDAO = new UserDAO();
        
        user = userDAO.login(rootId, rootPWD);
        if(user == null) {
            System.out.println("로그인 실패");
            JOptionPane.showMessageDialog(null, "아이디와 비밀번호를 확인해 주십시오.", "경고", JOptionPane.ERROR_MESSAGE);
        } else {
            System.out.println("로그인 성공");
            JOptionPane.showMessageDialog(null, "로그인 성공");
        }
    }//GEN-LAST:event_btnRootLoginActionPerformed

